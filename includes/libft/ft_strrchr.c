#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p;

	p = NULL;
	while (*s != 0)
	{
		if (*s == c % 256)
		{
			p = (char *)s;
		}
		s++;
	}
	if (p != NULL)
		return (p);
	if (c % 256 == 0)
		return ((char *)s);
	return (0);
}
